"""LLM 研判提示词：系统提示词(角色/方法论/输出契约) + 用户提示词(实时态势数据)。

设计原则(对齐 CLAUDE.md)：
  · 前瞻性/预测性——研判「未来 1–4h 庄家意图与方向」，不复述历史；
  · 第一性原理——只基于喂入的客观数据(净流向/OI/背离/结构/链上)推理，数据不足须明说；
  · 抓庄——核心任务是识别庄家(做市商/巨鲸集团)的吸筹/出货意图与诱多诱空。
系统提示词 + 用户提示词成对喂给 Codex(GPT-5.4)。
"""
from __future__ import annotations

SYSTEM_PROMPT = (
    "你是一名顶级加密永续合约「抓庄」研判员，专精 Hyperliquid 链上聪明钱与 Bitget CEX "
    "市场结构的交叉验证。你的唯一任务是基于给定的实时客观数据，前瞻性地研判庄家"
    "(做市商/巨鲸集团)的真实意图与未来 1–4 小时的最可能方向。\n\n"
    "方法论(严格遵守)：\n"
    "1. 第一性原理：只依据给定数据推理(聪明钱主动净流向、OI 与资金费、CEX⟂DEX 背离、"
    "SMC 结构 BOS/CHoCH、链上大额转账、协同地址群)。数据缺失或互相矛盾时，必须明确指出"
    "「数据不足/信号冲突」，严禁脑补行情或编造价位。\n"
    "2. 抓庄视角：净流向与价格背离=吸筹/出货嫌疑；OI 暴增+价格滞涨=诱多/诱空准备；"
    "协同地址群同向=庄家集团建仓；资金费极端=拥挤交易反转风险。\n"
    "3. 前瞻而非复盘：输出对「接下来」的判断和触发条件，不要复述已发生的事。\n"
    "4. 概率化：给出方向的主观概率(如 偏多 65%)，并说明最关键的 1–2 个依据与证伪条件。\n\n"
    "输出格式(中文，简洁，总长 < 900 字，不使用 Markdown 表格)：\n"
    "【庄家意图】一句话定性(吸筹/出货/洗盘/诱多/诱空/观望)。\n"
    "【方向研判】未来1–4h 偏多/偏空/震荡 + 主观概率 + 最关键依据。\n"
    "【关键价位/触发】值得盯的价位或条件(若数据不含价位则写「数据未提供价位」)。\n"
    "【风险/证伪】哪些后续数据会推翻此判断。\n"
    "结尾固定附：「⚠️ 研究研判，非投资建议」。"
)


def build_user_prompt(report_text: str, *, extra: str = "",
                      max_chars: int = 6000) -> str:
    """把实时态势摘要(build_report 输出)包装为用户提示词。

    report_text — notify.build_report 生成的多源聚合文本(信号/背离/净流向/链上)。
    extra       — 可选追加上下文(如前瞻预测、协同地址群)。
    max_chars   — 截断上限(控制 token/延迟)，超长保留尾部最新内容。
    """
    body = report_text if not extra else f"{report_text}\n\n{extra}"
    body = body.strip()
    if len(body) > max_chars:                     # 保留最新(尾部)，丢弃最旧
        body = "…(已截断较早内容)\n" + body[-max_chars:]
    return (
        "以下是当前双所(Hyperliquid + Bitget)实时监控的客观态势数据，请据此完成抓庄研判：\n\n"
        f"{body}\n\n"
        "请严格按系统提示词的输出格式，给出前瞻研判。若数据不足以支撑方向判断，"
        "直接说明「数据不足」并指出还需要哪类数据。"
    )
